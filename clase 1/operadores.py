""" operación         símbolo 

adición              +
sustracción          -
multiplicación       *
división             /
integer division     //
potenciación         **
modulo                % """


valor1 = 3
valor2 = 2

suma = valor1 + valor2

print(f'Resultado de la suma: {suma}')

resta = valor1 - valor2

print(f'Resultado de la resta: {resta}')

multi = valor1 * valor2

print(f'Resultado de la multiplicación: {multi}')

divi = valor1 / valor2

print(f'Resultado de la división: {divi}')

integer = valor1 // valor2

print(f'Resultado de la integer: {integer}')

pote = valor1 ** valor2

print(f'Resultado de la potenciación: {pote}')

modulo = valor1 % valor2

print(f'Resultado de la modulo: {modulo}')